CREATE SCHEMA IF NOT EXISTS SRC_SCHEMA;

DROP TABLE IF EXISTS SRC_SCHEMA.SRC_TABLE;

CREATE TABLE SRC_SCHEMA.SRC_TABLE (
    ID INTEGER NOT NULL,
    INTEGER_TYPE INTEGER,
    UNSIGNED_INT_TYPE UNSIGNED_INT,
    BIGINT_TYPE BIGINT,
    UNSIGNED_LONG_TYPE UNSIGNED_LONG,
    TINYINT_TYPE TINYINT,
    UNSIGNED_TINYINT_TYPE UNSIGNED_TINYINT,
    SMALLINT_TYPE SMALLINT,
    UNSIGNED_SMALLINT_TYPE UNSIGNED_SMALLINT,
    FLOAT_TYPE FLOAT,
    UNSIGNED_FLOAT_TYPE UNSIGNED_FLOAT,
    DOUBLE_TYPE DOUBLE,
    UNSIGNED_DOUBLE_TYPE UNSIGNED_DOUBLE,
    DECIMAL_TYPE DECIMAL(10,2),
    BOOLEAN_TYPE BOOLEAN,
    TIME_TYPE TIME,
    DATE_TYPE DATE,
    TIMESTAMP_TYPE TIMESTAMP,
    UNSIGNED_TIME_TYPE UNSIGNED_TIME,
    UNSIGNED_TIMESTAMP_TYPE UNSIGNED_TIMESTAMP,
    VARCHAR_TYPE VARCHAR(225),
    CHAR_TYPE CHAR(4),
    BINARY_TYPE BINARY(4),
    VARBINARY_TYPE VARBINARY,
    CONSTRAINT PK_ID PRIMARY KEY (ID)
);